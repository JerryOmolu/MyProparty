  <h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> What is My Proparty?</b></h1>        
   <h2> MY PROPARTY is a unique Item Information Management System, which enables fast registration, storage, and retrieval of data regarding Mobile Phones, Vehicles, Laptops and other items. It is an easy-to-operate solution that simplifies the process.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How does it work?</b></h1>        
  <h2> If you wish to register your items on the dashboard of My Proparty, it's easy to sign up and create an individual account. Provide the relevant information and the ID of each item - such as IMEI for phones, chassis number for vehicles or serial numbers of anything else. Once that is complete, verification can be done from the home page. To verify item(s) Click <a href="https://myproparty.com.ng/verify" style="color:blue">Verify Item</a>. </h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> Where can i Register my Item(s)?</b></h1>        
  <h2> Once you sign up for an Individual Account, you can register Item(s) on your own by using the Register item option on the menu bar of your dashboard.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How Long does it take to register an Item?</b></h1>        
  <h2> Processing time for registering any item is generally 3 minutes for an economical fee. Your operational skills and internet speed may reduce this duration.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How do i confirm my item registration?</b></h1>        
  <h2> Once the Menu Bar has been clicked on, select 'Verify Item', select an Item to be verified. After this, enter the Unique Number (such as IMEI for Mobile Phones, Chassis Number for Vehicles and so on) in the field provided upon registration. Alternatively, clicking <a href="https://myproparty.com.ng/verify" style="color:blue">Verify Item</a> is also an option. </h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How do i get information of an item that the Unique number (IMEI, Serial or Chassis number) is forgotten or misplaced?</b></h1>     
  <h2> If you already have an account, log in and view the items on the menu bar. Otherwise, visit a nearby My Proparty operator with your phone number to retrieve the needed information. </h2>
<br>
</div>
<div class="col-md-6" align="justify">
  <h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> Can i register an item on behalf of a third party?</b></h1>        
  <h2> Provide a My Proparty Operator with the person's information and a photocopy of their ID, along with the item you'd like to register. Registration on behalf of another cannot be done independently - it must be done through an Operator as each registration will be tied to the individual who is responsible for it.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> With event of any error or mistake on Item Registration, How do i go about it?</b></h1>        
  <h2> Go to any My Proparty Operator near you and present your case. It will be attended to as fast as possible. or go to our contact page '<a href="https://myproparty.com.ng/contactUs" style="color:blue">Contact Us</a>' to present your case</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How do i effect a change of ownership on a registered item?</b></h1>        
  <h2> Bring a copy of a Sworn Court affidavit and the receipt for the item to your nearest My Proparty Operator. The documents will be checked and ratified by an Admin, who will then arrange for ownership to be transferred. An Admin must carry out the change of ownership, but it should be requested through an Operator.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> How do i report a stolen Item?</b></h1>        
  <h2> To report a stolen item, one must log into their dashboard, choose the item under view items, and click Report Item. An Admin will assess before approval. After verifying that the item has been taken, its status will be updated to 'Stolen'.</h2>
<br>
<h1 style="color:green"><b><i class="fa fa-question-circle" aria-hidden="true"></i> Can i report an Item that is not registered on My Proparty?</b></h1>        
  <h2> You will need to provide both a police report and the item's unique identifier - such as an IMEI for mobile phones and a chassis number for vehicles - in order to receive any assistance. Unfortunately, without this information, recovery of the stolen item is highly unlikely. </h2>
